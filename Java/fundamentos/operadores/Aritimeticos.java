package fundamentos.operadores;

public class Aritimeticos {
	
	public static void main(String[] args) {
		
		var x = 32.2;
		double y = 412.2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int z = 8;
		int c = 3;
		
		System.out.println(z + c);
		System.out.println(z - c);
		System.out.println(z * c);
		System.out.println(z / c);
		
	}

}
